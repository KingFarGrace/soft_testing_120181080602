package homework_3_15;

public class Calculator {
	
	public double add(double a, double b) {
		return a + b;
	}
	
	public double minus(double a, double b) {
		return a - b;
	}
	
	public double multiple(double a, double b) {
		return a * b;
	}
	
	public double devide(double a, double b) {
		return a / b;
	}

}
